CREATE DATABASE project;
CREATE TABLE EMPLOYEE(
Employee_ID VARCHAR (20) PRIMARY KEY,
Employee_Name VARCHAR(20) NOT NULL,
Employee_Type VARCHAR(20) NOT NULL,
Employee_Scale VARCHAR(20) NOT NULL,
Employee_Title VARCHAR (20) NOT NULL
);
CREATE TABLE CATEGORY
(
Category_ID VARCHAR (20) PRIMARY KEY,
Category_Name VARCHAR (20) NOT NULL,
P_Description VARCHAR (20) NOT NULL
);

CREATE TABLE PRODUCT
(
Product_ID VARCHAR (20) PRIMARY KEY,
Product_Description VARCHAR (20) NOT NULL,
Product_Name VARCHAR (20) NOT NULL,

Product_Price INT NOT NULL,
Product_In_Stock VARCHAR (20) NOT NULL,
Category_ID VARCHAR (20) NOT NULL,

FOREIGN KEY (Category_ID) REFERENCES CATEGORY(Category_ID)
);
CREATE TABLE PAYMENT
(
Bill_No INT PRIMARY KEY,
Payment_Type VARCHAR (20) NOT NULL,
Amount INT NOT NULL,
Bill_Issue_Date DATE NOT NULL,
P_Due_Date DATE NOT NULL

);
CREATE TABLE ORDER_DETAILS
(
Order_Details_ID VARCHAR (20) PRIMARY KEY,
P_ID VARCHAR(20) NOT NULL,

Order_Quantity INT,
Order_Date DATE NOT NULL,
Discount VARCHAR(5) NOT NULL,
Bill_No INT NOT NULL,

FOREIGN KEY (Bill_No) REFERENCES PAYMENT (Bill_No),
FOREIGN KEY (P_ID) REFERENCES PRODUCT (Product_ID)

);
CREATE TABLE SUPPLIER
(
Supplier_ID VARCHAR (20) PRIMARY KEY,
Supplier_Name VARCHAR (20) NOT NULL,
S_Pay_Method VARCHAR (20) NOT NULL,
S_Discount INT,

S_Product_Total INT
);

CREATE TABLE CUSTOMER
(
Customer_ID VARCHAR (20) PRIMARY KEY,
Customer_Name VARCHAR (20) NOT NULL,
C_Pay_Method VARCHAR(20) NOT NULL,
Customer_Email VARCHAR (20),
Staff_ID VARCHAR (20) NOT NULL,

FOREIGN KEY (Staff_ID) REFERENCES EMPLOYEE(Employee_ID)
);

CREATE TABLE ORDER_
(
Order_ID VARCHAR(20) PRIMARY KEY,
Order_Date DATE NOT NULL,
Order_Details_ID VARCHAR(20) NOT NULL,
Staff_ID VARCHAR (20) NOT NULL,
FOREIGN KEY (Staff_ID) REFERENCES EMPLOYEE (Employee_ID),
FOREIGN KEY (Order_Details_ID) REFERENCES
ORDER_DETAILS(Order_Details_ID)
);
CREATE TABLE CUSTOMER_PHONE_NO
(
Phone_no INT NOT NULL,
Customer_ID VARCHAR (20) PRIMARY KEY,
FOREIGN KEY (Customer_ID) REFERENCES
CUSTOMER(CUSTOMER_ID)

);
CREATE TABLE EMPLOYEE_PHONE_NO
(
E_Phone_no INT NOT NULL,
Employee_ID VARCHAR (20) PRIMARY KEY,
FOREIGN KEY (Employee_ID) REFERENCES
EMPLOYEE(EMPLOYEE_ID)
);
CREATE TABLE SUPPLIER_PHONE_NO
(
Phone_no INT NOT NULL,
Supplier_ID VARCHAR (20) PRIMARY KEY,
FOREIGN KEY (Supplier_ID) REFERENCES SUPPLIER(SUPPLIER_ID)
);
CREATE TABLE CONTAINS
(
Order_Details_ID VARCHAR (20) NOT NULL,
Product_ID VARCHAR (20) NOT NULL,
FOREIGN KEY (Order_Details_ID) REFERENCES
ORDER_DETAILS(Order_Details_ID),
FOREIGN KEY (Product_ID) REFERENCES
PRODUCT(Product_ID)
);

CREATE TABLE SUPPLIES
(
Supplier_ID VARCHAR(20) NOT NULL,
Product_ID varchar(20) NOT NULL,
FOREIGN KEY (Supplier_ID) REFERENCES
SUPPLIER(Supplier_ID),
FOREIGN KEY (Product_ID) REFERENCES
PRODUCT(Product_ID)
);

